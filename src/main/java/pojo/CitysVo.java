package pojo;

public class CitysVo {
    private String date;
    private String srcIP;
    private String disIP;
    private String srcAdd;
    private String disAdd;
    private String type;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSrcIP() {
        return srcIP;
    }

    public void setSrcIP(String srcIP) {
        this.srcIP = srcIP;
    }

    public String getDisIP() {
        return disIP;
    }

    public void setDisIP(String disIP) {
        this.disIP = disIP;
    }

    public String getSrcAdd() {
        return srcAdd;
    }

    public void setSrcAdd(String srcAdd) {
        this.srcAdd = srcAdd;
    }

    public String getDisAdd() {
        return disAdd;
    }

    public void setDisAdd(String disAdd) {
        this.disAdd = disAdd;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
